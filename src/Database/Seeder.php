    public function __construct(
        private readonly PDO $pdo,
    ) {
        $this->faker = Factory::create('ru_RU');
    }

    public function run(bool $force = false): void
    {
        if ($this->hasData()) {
            if (!$force) {
                echo "Database already contains data. Skipping seed.\n";
                echo "Use: php bin/seed.php --force\n";

                return;
            }

            $this->truncate();
            echo "Existing data cleared.\n";
        }

        $categories = $this->seedCategories();
        $this->seedArticles($categories);

        echo "Seeding completed.\n";
    }

    private function hasData(): bool
    {
        return (int) $this->pdo->query('SELECT COUNT(*) FROM categories')->fetchColumn() > 0;
    }

    private function truncate(): void
    {
        $this->pdo->exec('SET FOREIGN_KEY_CHECKS = 0');
        $this->pdo->exec('TRUNCATE TABLE article_category');
        $this->pdo->exec('TRUNCATE TABLE articles');
        $this->pdo->exec('TRUNCATE TABLE categories');
        $this->pdo->exec('SET FOREIGN_KEY_CHECKS = 1');
    }

    private function seedCategories(): array
    {
        $categories = [
            ['name' => 'Технологии', 'slug' => 'technology', 'description' => 'Новости и обзоры из мира IT'],
            ['name' => 'Путешествия', 'slug' => 'travel', 'description' => 'Маршруты, советы и впечатления'],
            ['name' => 'Кулинария', 'slug' => 'cooking', 'description' => 'Рецепты и кулинарные лайфхаки'],
            ['name' => 'Спорт', 'slug' => 'sport', 'description' => 'Соревнования, тренировки и здоровый образ жизни'],
            ['name' => 'Книги', 'slug' => 'books', 'description' => 'Рецензии и подборки для чтения'],
        ];

        $stmt = $this->pdo->prepare(
            'INSERT INTO categories (name, slug, description, created_at) VALUES (:name, :slug, :description, :created_at)',
        );

        $result = [];

        foreach ($categories as $category) {
            $stmt->execute([
                'name' => $category['name'],
                'slug' => $category['slug'],
                'description' => $category['description'],
                'created_at' => $this->faker->dateTimeBetween('-1 year', 'now')->format('Y-m-d H:i:s'),
            ]);
            $result[(int) $this->pdo->lastInsertId()] = $category['name'];
        }

        return $result;
    }

    private function seedArticles(array $categories): void
    {
        $articleStmt = $this->pdo->prepare(
            'INSERT INTO articles (title, slug, description, content, image, views_count, published_at, created_at)
             VALUES (:title, :slug, :description, :content, :image, :views_count, :published_at, :created_at)',
        );

        $linkStmt = $this->pdo->prepare(
            'INSERT INTO article_category (article_id, category_id) VALUES (:article_id, :category_id)',
        );

        foreach ($categories as $categoryId => $categoryName) {
            foreach (self::ARTICLES_BY_CATEGORY[$categoryName] as $article) {
                $slug = $this->uniqueSlug(Slugger::slugify($article['title']));
                $publishedAt = $this->faker->dateTimeBetween('-6 months', 'now');

                $articleStmt->execute([
                    'title' => $article['title'],
                    'slug' => $slug,
                    'description' => $article['description'],
                    'content' => $article['content'],
                    'image' => 'https://picsum.photos/seed/' . rawurlencode($slug) . '/800/450',
                    'views_count' => $this->faker->numberBetween(50, 5000),
                    'published_at' => $publishedAt->format('Y-m-d H:i:s'),
                    'created_at' => $publishedAt->format('Y-m-d H:i:s'),
                ]);

                $linkStmt->execute([
                    'article_id' => (int) $this->pdo->lastInsertId(),
                    'category_id' => $categoryId,
                ]);
            }
        }
    }

    private function uniqueSlug(string $base): string
    {
        $slug = $base;
        $suffix = 1;

        $stmt = $this->pdo->prepare('SELECT COUNT(*) FROM articles WHERE slug = :slug');

        while (true) {
            $stmt->execute(['slug' => $slug]);
            if ((int) $stmt->fetchColumn() === 0) {
                return $slug;
            }
            $slug = $base . '-' . $suffix++;
        }
    }
